int		ft_str_is_alpha(char *str)
{
	int i;
	int alpha;

	i = 0;
	alpha = 1;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
				|| (str[i] >= 'A' && str[i] <= 'Z'))
			alpha = 1;
		else
		{
			return (0);
		}
		i++;
	}
	return (alpha);
}
