void		ft_putchar(char c);

void		ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putnbr(-nb / 10);
		ft_putchar('8');
	}
	else if (nb < 0)
	{
		ft_putchar('-');
		ft_putnbr(-nb);
	}
	else if (nb < 10)
		ft_putchar(nb + '0');
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
